CREATE TABLE projects (
    id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT      NOT NULL,
    name        VARCHAR(15) NOT NULL,
    title_count SMALLINT    NOT NULL DEFAULT 0,
    is_deleted  BOOLEAN     NOT NULL DEFAULT FALSE,
    deleted_at  TIMESTAMPTZ,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_projects_user FOREIGN KEY (user_id) REFERENCES users(id)
);
CREATE INDEX        idx_projects_user_is_deleted       ON projects (user_id, is_deleted);
CREATE UNIQUE INDEX uq_projects_user_name_is_deleted   ON projects (user_id, name, is_deleted);


CREATE TABLE scrum_titles (
    id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT      NOT NULL,
    project_id  BIGINT      NOT NULL,
    free_text   VARCHAR(20) NOT NULL,
    scrum_count SMALLINT    NOT NULL DEFAULT 0,
    is_deleted  BOOLEAN     NOT NULL DEFAULT FALSE,
    deleted_at  TIMESTAMPTZ,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_scrum_titles_user    FOREIGN KEY (user_id)    REFERENCES users(id),
    CONSTRAINT fk_scrum_titles_project FOREIGN KEY (project_id) REFERENCES projects(id)
);
CREATE INDEX idx_scrum_titles_user_is_deleted    ON scrum_titles (user_id, is_deleted);
CREATE INDEX idx_scrum_titles_project_is_deleted ON scrum_titles (project_id, is_deleted);


CREATE TABLE scrums (
    id                  BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT      NOT NULL,
    title_id            BIGINT      NOT NULL,
    content             VARCHAR(50) NOT NULL,
    scrum_date          DATE        NOT NULL,
    has_star            BOOLEAN     NOT NULL DEFAULT FALSE,
    selected_competency VARCHAR(30),
    is_deleted          BOOLEAN     NOT NULL DEFAULT FALSE,
    deleted_at          TIMESTAMPTZ,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_scrums_user                FOREIGN KEY (user_id)  REFERENCES users(id),
    CONSTRAINT fk_scrums_title               FOREIGN KEY (title_id) REFERENCES scrum_titles(id),
    CONSTRAINT ck_scrums_selected_competency CHECK (
        selected_competency IS NULL OR selected_competency IN (
            'DISCOVERY_ANALYSIS', 'PLANNING_EXECUTION', 'COLLABORATION', 'PROBLEM_SOLVING', 'REFLECTION_GROWTH'
        )
    )
    -- NOTE: scrum_date 범위(당일 기준 최근 14일 이내, 미래 불가) CHECK은 CURRENT_DATE가 STABLE이라 불가 → 앱 레벨 검증
);
CREATE INDEX idx_scrums_user_scrum_date            ON scrums (user_id, scrum_date);
CREATE INDEX idx_scrums_title_is_deleted           ON scrums (title_id, is_deleted);
CREATE INDEX idx_scrums_user_scrum_date_is_deleted ON scrums (user_id, scrum_date, is_deleted);
CREATE INDEX idx_scrums_user_created_at            ON scrums (user_id, created_at);


