-- =========================================================
-- NOTIFICATION
-- =========================================================
CREATE TABLE notification_settings (
    id          BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT      NOT NULL,
    day_of_week VARCHAR(10) NOT NULL,
    notify_time TIME        NOT NULL,
    is_active   BOOLEAN     NOT NULL DEFAULT TRUE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_notification_settings_user        FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT ck_notification_settings_day_of_week CHECK (
        day_of_week IN ('MON', 'TUE', 'WED', 'THU', 'FRI', 'SAT', 'SUN')
    ),
    -- notify_time 허용 슬롯: 00:00 (자정) 또는 07:00~23:30 중 30분 단위
    CONSTRAINT ck_notification_settings_notify_time CHECK (
        (notify_time >= TIME '07:00' OR notify_time = TIME '00:00')
        AND EXTRACT(MINUTE FROM notify_time) IN (0, 30)
        AND EXTRACT(SECOND FROM notify_time) = 0
    )
);
CREATE UNIQUE INDEX uq_notification_settings_user_day_time ON notification_settings (user_id, day_of_week, notify_time);
CREATE INDEX        idx_notification_settings_day_time_active ON notification_settings (day_of_week, notify_time, is_active);


