-- =========================================================
-- V1__init_schema.sql
-- GLIT 커리어 기록 서비스 초기 스키마
--
-- 정책:
--  - Enum: PostgreSQL native enum 대신 VARCHAR + CHECK 제약 (Hibernate @Enumerated STRING 호환)
--  - Timestamp: 전부 TIMESTAMPTZ (UTC 저장, 엔티티 OffsetDateTime과 매핑)
--  - BaseTimeEntity / SoftDeleteEntity @MappedSuperclass → 각 테이블에 인라인 컬럼으로 펼침
--  - append-only 테이블(user_term_agreements) UPDATE/DELETE 금지는 애플리케이션 레벨 규약
--  - 시간 의존 CHECK(scrums.scrum_date 범위 등)은 PG IMMUTABLE 제약으로 표현 불가 → 애플리케이션 레벨 검증
-- =========================================================


-- =========================================================
-- USER
-- =========================================================
CREATE TABLE users (
    id             BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nickname       VARCHAR(12)  NOT NULL,
    job_role       VARCHAR(20)  NOT NULL,
    user_status    VARCHAR(20)  NOT NULL,
    branding_title VARCHAR(100),
    last_login_at  TIMESTAMPTZ,
    hard_delete_at TIMESTAMPTZ,
    is_deleted     BOOLEAN      NOT NULL DEFAULT FALSE,
    deleted_at     TIMESTAMPTZ,
    created_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT ck_users_job_role    CHECK (job_role    IN ('PLANNER', 'DEVELOPER', 'DESIGNER')),
    CONSTRAINT ck_users_user_status CHECK (user_status IN ('STUDENT', 'JOB_SEEKER', 'EMPLOYED'))
);
CREATE INDEX idx_users_nickname          ON users (nickname);
CREATE INDEX idx_users_active_last_login ON users (is_deleted, last_login_at);


-- =========================================================
-- AUTH
-- =========================================================
CREATE TABLE social_accounts (
    id           BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id      BIGINT       NOT NULL,
    provider     VARCHAR(20)  NOT NULL,
    provider_uid VARCHAR(255) NOT NULL,
    email        VARCHAR(255),
    created_at   TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at   TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT fk_social_accounts_user     FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT ck_social_accounts_provider CHECK (provider IN ('KAKAO', 'GOOGLE', 'NAVER'))
);
CREATE UNIQUE INDEX uq_social_accounts_provider_uid  ON social_accounts (provider, provider_uid);
CREATE UNIQUE INDEX uq_social_accounts_user_provider ON social_accounts (user_id, provider);


CREATE TABLE auth_tokens (
    id                 BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id            BIGINT       NOT NULL,
    refresh_token_hash VARCHAR(255) NOT NULL,
    device_id          VARCHAR(100),
    expires_at         TIMESTAMPTZ  NOT NULL,
    revoked_at         TIMESTAMPTZ,
    created_at         TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at         TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT fk_auth_tokens_user FOREIGN KEY (user_id) REFERENCES users(id)
);
CREATE UNIQUE INDEX uq_auth_tokens_refresh_token_hash ON auth_tokens (refresh_token_hash);
CREATE INDEX        idx_auth_tokens_user_revoked_at  ON auth_tokens (user_id, revoked_at);
CREATE INDEX        idx_auth_tokens_expires_at       ON auth_tokens (expires_at);


CREATE TABLE device_tokens (
    id         BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id    BIGINT       NOT NULL,
    platform   VARCHAR(20)  NOT NULL,
    push_token VARCHAR(255) NOT NULL,
    is_active  BOOLEAN      NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT fk_device_tokens_user     FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT ck_device_tokens_platform CHECK (platform IN ('IOS', 'ANDROID', 'WEB'))
);
CREATE UNIQUE INDEX uq_device_tokens_push_token    ON device_tokens (push_token);
CREATE INDEX        idx_device_tokens_user_active ON device_tokens (user_id, is_active);


-- =========================================================
-- RECORD: Project / ScrumTitle / Scrum
