-- append-only (앱 레벨에서 UPDATE/DELETE 금지 규약)
CREATE TABLE user_term_agreements (
    id              BIGINT       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id         BIGINT       NOT NULL,
    term_version_id BIGINT       NOT NULL,
    agreed          BOOLEAN      NOT NULL,
    agreed_at       TIMESTAMPTZ  NOT NULL,
    ip_address      VARCHAR(45),
    user_agent      VARCHAR(500),
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT fk_user_term_agreements_user         FOREIGN KEY (user_id)         REFERENCES users(id),
    CONSTRAINT fk_user_term_agreements_term_version FOREIGN KEY (term_version_id) REFERENCES term_versions(id)
);
CREATE INDEX idx_user_term_agreements_user_term_created
    ON user_term_agreements (user_id, term_version_id, created_at);
CREATE INDEX idx_user_term_agreements_user_agreed_at
    ON user_term_agreements (user_id, agreed_at);
CREATE INDEX idx_user_term_agreements_term_version
    ON user_term_agreements (term_version_id);


